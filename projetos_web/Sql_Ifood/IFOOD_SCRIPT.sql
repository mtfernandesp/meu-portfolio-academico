CREATE DATABASE IF NOT EXISTS IFOOD_DB;

USE IFOOD_DB;

CREATE TABLE IF NOT EXISTS IFOOD_DB.CLIENTE
(
IDCLIENTE INT AUTO_INCREMENT NOT NULL,
NOMECLIENTE VARCHAR(100) NOT NULL,
NUMCPFCLIENTE CHAR(11),
EMAIL VARCHAR(110) NOT NULL, 
TELEFONE CHAR(11),
PRIMARY KEY (IDCLIENTE),
UNIQUE (NUMCPFCLIENTE)
);

CREATE TABLE IF NOT EXISTS IFOOD_DB.ENTREGADOR
(
IDENTREGADOR INT AUTO_INCREMENT NOT NULL,
NUMCPFENTREGADOR CHAR(11),
NOMEENTREGADOR VARCHAR(100),
TIPOVEICULO ENUM('MOTO','BICICLETA'),
PLACA VARCHAR(7),
PRIMARY KEY (IDENTREGADOR)
);

CREATE TABLE IF NOT EXISTS IFOOD_DB.PRODUTO
(
IDPRODUTO INT AUTO_INCREMENT NOT NULL,
NOMEPROD VARCHAR(100) NOT NULL,
DESCRICAO VARCHAR(150) NOT NULL,
PRECOUNI DECIMAL(10,2) NOT NULL,
PRIMARY KEY (IDPRODUTO)
);

CREATE TABLE IF NOT EXISTS IFOOD_DB.PEDIDO
(
IDPEDIDO INT AUTO_INCREMENT NOT NULL,
IDCLIENTE INT NOT NULL,
IDENTREGADOR INT NOT NULL,
ENTREGAPREVISTA DATETIME NOT NULL,
ENTREGAREALIZADA DATETIME,
PRECOFINAL DECIMAL(11,2),
PRIMARY KEY (IDPEDIDO)
);

ALTER TABLE IFOOD_DB.PEDIDO 
ADD CONSTRAINT FK_CLIENTE 
FOREIGN KEY (IDCLIENTE) REFERENCES IFOOD_DB.CLIENTE(IDCLIENTE);

ALTER TABLE IFOOD_DB.PEDIDO 
ADD CONSTRAINT FK_ENTREGADOR 
FOREIGN KEY (IDENTREGADOR) REFERENCES IFOOD_DB.ENTREGADOR(IDENTREGADOR);

